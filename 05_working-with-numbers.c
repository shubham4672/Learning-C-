#include <stdio.h>
#include <math.h>
int main()
{
    printf("%f\n", 8.9); // f means decimal number
    printf("%f\n", 5 + 4.9);
    printf("%f\n", 5.9 + 5.5);
    printf("%f\n", 5.9 * 5.5);
    printf("%f\n", 5.9 / 5.5);
    printf("%f\n", 5.0 / 4);
    // math functions
    printf("%f\n", pow(2, 3));
    printf("%f\n", sqrt(36));
    printf("%f\n", ceil(36.256));
    return 0;
}

